test('random', t => {
  var buf, value;
  for(var i = 1000; i-- > 0; ) {
    value = Math.round(Math.random()*bufconv.MAX_ALLOWED_INTEGER);
    buf = bufconv.allocBufUIntLE(value);
    t.strictEquals(buf.length,  Math.ceil(Math.log2(value + 1)/8));
    t.strictEquals(bufconv.readBufUIntLE(buf), value);
  }
  for(var i = 1000; i-- > 0; ) {
    value = Math.round(Math.random()*bufconv.MAX_ALLOWED_INTEGER + 1);
    buf = bufconv.allocBufIntLE(value);
    t.strictEquals(buf.length,  Math.ceil(Math.log2(value*2 + 1)/8));
    t.strictEquals(bufconv.readBufIntLE(buf), value);
    value = Math.round(Math.random()*bufconv.MIN_ALLOWED_INTEGER - 1);
    buf = bufconv.allocBufIntLE(value);
    t.strictEquals(buf.length,  Math.ceil(Math.log2(-value*2)/8));
    t.strictEquals(bufconv.readBufIntLE(buf), value);
  }
  for(var i = 1000; i-- > 0; ) {
    value = Math.random()*bufconv.MAX_ALLOWED_INTEGER + 1;
    buf = bufconv.allocBufNumberLE(value);
    t.strictEquals(buf.length, value % 1 === 0 ? Math.ceil(Math.log2(value*2 + 1)/8) : 8);
    t.strictEquals(bufconv.readBufNumberLE(buf), value);
    value = Math.random()*bufconv.MIN_ALLOWED_INTEGER - 1;
    buf = bufconv.allocBufNumberLE(value);
    t.strictEquals(buf.length, value % 1 === 0 ? Math.ceil(Math.log2(-value*2)/8) : 8);
    t.strictEquals(bufconv.readBufNumberLE(buf), value);
  }
  t.end();
});

